package com.homedepot.di.xd.efs.to;

public enum ReportTypeCodes
{
	PAYMENT_VENDOR_REPORT_TYPE_CODE ((short)1),
	CARRIER_CODE_REPORT_TYPE_CODE ((short)2);
	private final Short reportTypeCode;
	
	private ReportTypeCodes(Short reportTypeCode)
	{
		this.reportTypeCode = reportTypeCode;
	}
	/**
	 * 	
	 * @return the reportTypeCode
	 */
	public Short getReportTypeCode()
	{
		return this.reportTypeCode;
	}

}
